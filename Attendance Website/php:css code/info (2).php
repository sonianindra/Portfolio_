    <h1>Attendance Rules</h1>
    
    <p><strong>How many points do I need to be an active brother?</strong><br>
    You need to have at least 45 points.</p>

    <p><strong>How many points do I need from each type of event?</strong><br>
    Each brother is required to have 9 points for philanthropy, professional, and DEI events. The remaining 18 points are earned through other general events (chapter, townhall, rush, etc).</p>

    <p><strong>How many points are earned from each event?</strong><br>
    Major events = 6 points.<br>
    Minor events = 3 points.<br>
    Chapter = 1 point.<br>
    Town Hall = 3 points.<br>
    There will be other events worth 1/2 point as well.</p>

    <p><strong>How do I get points from rush events?</strong><br>
    Each rush event is 1 point , besides the bid event which is 0. By the first round of voting, you must have attended 4/7 rush events. To be able to participate in final voting, you must have attended 5/7 rush events. </p>

    <p><strong>What events are mandatory?</strong><br>
    - Missing Inductions without a valid excuse will result in -3 points, and you will not be allowed to attend formal as a brother.<br>
    - Missing Initiation without a valid excuse will result in -3 points, and you will not be allowed to attend formal as a brother.<br>
    - Missing chapter without a valid excuse will result in -1 point.</p>

    <p><strong>What should I do if I can’t go to an event?</strong><br>
    If you are not able to attend an event (for a valid reason), please fill out the half point request form by 10:00 PM the night before the event, you can get half points until you have reached 15 points. This doesn’t apply to chapter,town hall or rush events for which there are no half points</p>
    
    <p><strong>Can I be late to an event or leave early?</strong><br>
     With an approved valid excuse, you can miss 1/4 of an event and still receive full points, however you must text me in advance. For example if the event is an hour long, in order to receive points you would only be allowed to miss 15 minutes, anything more would result in no points<br> Please Note that: <br>
     - You cannot receive half points by going to half an event, half points excuse you from the full event.<br>
     - For some events that have outside speakers, late entry will not be acceptable, in that case I would tell you to just fill out the half points form and not come to the event</p>
    
     <p><strong>What should I do if I can’t go to chapter?</strong><br>
    -If you have a reoccuring conflict during Chapter, please fill out the the reoccuring absence form located at the top once, from then on you will be excused from chapter and do not have to let me know every time.<br>
    -If you can't make it to a certain chapter, please text Sonia by 10pm the night before to avoid the -1 point deduction.</p>

    <p><strong>Where can I find my points?</strong><br>
    All points/attendance related things can be found at pctumdattendance.com, once you log on you will be able to see your dashboard.</p>

    <p><strong>Where can I see the schedule?</strong><br>
    The calendar is now public, it is located on the website under the announcements tab and you can also add it to your personal GCal.</p>

    <p><strong>How can I go to brotherhood retreat?</strong><br>
    The top 10 brothers with the highest points get to attend brotherhood retreat. Exec only needs to fulfill the 45 points, chairs also need to fulfill their contract along with the 45 points, so you won’t see their names on the list. Brotherhood retreat is a ton of fun, so try to get as many points as you can!</p>

